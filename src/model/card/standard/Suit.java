package model.card.standard;

/**
 * Enum representing the four suits of standard playing cards in the Jackaroo game.
 * Suits are used in conjunction with standard cards to determine gameplay actions.
 */
public enum Suit {
    
    /** Represents the Heart suit. */
    HEART, 
    
    /** Represents the Diamond suit. */
    DIAMOND, 
    
    /** Represents the Spade suit. */
    SPADE, 
    
    /** Represents the Club suit. */
    CLUB
}
