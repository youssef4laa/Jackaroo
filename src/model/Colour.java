package model;

/**
 * Represents the four possible colours of the marbles in the Jackaroo game.
 * Each colour is associated with a player and their marbles, safe zones, and entry points.
 */
public enum Colour {
    
    /** Represents the red colour for marbles and player zones. */
    RED, 
    
    /** Represents the green colour for marbles and player zones. */
    GREEN, 
    
    /** Represents the blue colour for marbles and player zones. */
    BLUE, 
    
    /** Represents the yellow colour for marbles and player zones. */
    YELLOW
}
